package day27_nested_loops;

public class NestedLoopExample {

    public static void main(String[] args) {

        // hello word 5 times
        // hello universe 1 time
        // hello word 5 time
        // hello universe 1 time

        for (int j = 0; j < 3; j++) {


            for (int i = 0; i < 5; i++) {
                System.out.println("hello word");
            }
            System.out.println("Hello universe");

        }


    }



}
