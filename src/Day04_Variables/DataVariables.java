package Day04_Variables;

public class DataVariables {

    public static void main(String[] args) {
        int month, day, year;
        month = 7;
        day = 31;
        year = 2012;

        System.out.println("Date: " + month + "/" +  day + "/" + year );

        month = 8;
        day = 1;
        year = 2021;

        System.out.println("Tomorrows date:" + month + "/" +  day + "/" + year );

    }

}
