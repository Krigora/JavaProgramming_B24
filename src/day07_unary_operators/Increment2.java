package day07_unary_operators;

public class Increment2 {

    public static void main(String[] args) {

        int i = 5;
        i++; // i+6
        int x = i++; // int x =6

        System.out.println("x:" + x);
        System.out.println("i: " + i );

        int b = 10;
        int c = ++b;

        System.out.println("b: " + b );
        System.out.println(" c: " + c );






    }
}
